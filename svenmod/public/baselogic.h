template <class T, class A>
struct __IsSameTypeT
{
    static constexpr T value = false;
};

template <class T>
struct __IsSameTypeT<T, T>
{
    static constexpr T value = true;
};

template <class T, class A>
constexpr bool IsSameType = __IsSameTypeT<T, A>::value;

inline constexpr int hex_to_decimal_fast(char *hex)
{
	int result = 0;

	while (*hex && result >= 0)
	{
		result = (result << 4) | hex_to_decimal_table[*hex++];
	}

	return result;
}

